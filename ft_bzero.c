#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*arr;

	i = 0;
	arr = s;
	while (i < n)
	{
		*arr = 0;
		i++;
		arr++;
	}
}
