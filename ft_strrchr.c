#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	s_len;

	s_len = ft_strlen(s);
	while (s_len)
	{
		if (*s == (char)c)
			return ((char *)s);
		s_len--;
	}
	return (NULL);
}
